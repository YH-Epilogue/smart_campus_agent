def add_section(title_text, cases):
    doc.add_heading(title_text, level=2)
    table = doc.add_table(rows=1, cols=5, style='Table Grid')
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    hdr = table.rows[0].cells
    for i, h in enumerate(['编号', '测试项', '操作步骤', '预期结果', '结果']):
        hdr[i].text = h
    for idx, (item, steps, expected) in enumerate(cases, 1):
        row = table.add_row().cells
        row[0].text = f'{idx}'
        row[1].text = item
        row[2].text = steps
        row[3].text = expected
        row[4].text = '□通过 □失败'
    doc.add_paragraph()

